static int si = 1; /* SI by default */

static double udifftimeval(struct timeval start, struct timeval end)
{
    return (double)(end.tv_usec - start.tv_usec) + (double)(end.tv_sec - start.tv_sec) * 1000 * 1000;
}

static int must_finish = 0;

static void alarm_handler(int signo)
{
    must_finish = 1;
}

static char *units[]    = {"", "Ki", "Mi", "Gi", "Ti", 0};
static char *si_units[] = {"", "K", "M", "G", "T", 0};

static void value2human(int si, double bytes, double time, double *data, double *speed, char *metric)
{
    int unit = 0;

    *data = bytes;

    if (si)
    {
        while (*data > 1000 && si_units[unit + 1])
        {
            *data /= 1000;
            unit++;
        }
        *speed = *data / time;
        sprintf(metric, "%sB", si_units[unit]);
    }
    else
    {
        while (*data > 1024 && units[unit + 1])
        {
            *data /= 1024;
            unit++;
        }
        *speed = *data / time;
        sprintf(metric, "%sB", units[unit]);
    }
}

static int test_rsa(int fd, struct rsa_config *prsa_config)
{
    int i = 0;

#if 1
    // reset RSA
    if (ioctl(fd, MDrv_RSA_Reset, NULL))
    {
        perror("ioctl(MDrv_RSA_Reset)");
        return 1;
    }

    // RSA calculate
    if (ioctl(fd, MDrv_RSA_Calculate, prsa_config))
    {
        perror("ioctl(MDrv_RSA_Calculate)");
        return 1;
    }
#endif

    return 0;
}

int encrypt_data(int fd, int chunksize)
{
    static int        val = 23;
    struct timeval    start, end;
    double            total = 0;
    double            secs, ddata, dspeed;
    char              metric[16];
    struct rsa_config rsaConfig;

    char *in  = NULL;
    char *out = NULL;

    if (!(in = malloc(chunksize)))
    {
        perror("malloc()");
        return 1;
    }

    if (!(out = malloc(chunksize)))
    {
        perror("malloc()");
        return 1;
    }

    printf("\tEncrypting in chunks of %d bytes: ", chunksize);
    fflush(stdout);

    memset(in, val++, chunksize);
    memset(out, 0, chunksize);

    must_finish = 0;
    alarm(5);

    gettimeofday(&start, NULL);
    do
    {
        rsaConfig.pu32RSA_Sig    = (unsigned int *)in;
        rsaConfig.pu32RSA_Output = (unsigned int *)out;
        rsaConfig.pu32RSA_KeyN   = (unsigned int *)N;
        rsaConfig.pu32RSA_KeyE   = (unsigned int *)E;
        rsaConfig.u32RSA_KeyELen = sizeof(E) / 4;
        rsaConfig.u32RSA_KeyNLen = sizeof(N) / 4;
        rsaConfig.u32RSA_SigLen  = chunksize / 4;

        if (0 != test_rsa(fd, &rsaConfig))
        {
            printf("[%s:%d] test_rsa failed\n", __FUNCTION__, __LINE__);
            return 1;
        }

        total += chunksize;
    } while (must_finish == 0);
    gettimeofday(&end, NULL);

    secs = udifftimeval(start, end) / 1000000.0;

    value2human(si, total, secs, &ddata, &dspeed, metric);
    printf("done. %.2f %s in %.2f secs: ", ddata, metric, secs);
    printf("%.2f %s/sec\n", dspeed, metric);

    free(in);
    free(out);
    return 0;
}

int main(int argc, char **argv)
{
    int fd  = -1;
    int i   = 0;
    int ret = 0;

    signal(SIGALRM, alarm_handler);

    /* Open the crypto device */
    fd = open("/dev/rsa", O_RDWR, 0);
    if (fd < 0)
    {
        perror("open(/dev/rsa)");
        return 1;
    }

    fprintf(stderr, "\nTesting RSA cipher: \n");

    for (i = 256; i <= (64 * 1024); i *= 2)
    {
        if (encrypt_data(fd, i))
        {
            ret = 1;
            continue;
        }
    }
    // test_crypto(fd);

    /* Close the original descriptor */
    if (close(fd))
    {
        perror("close(fd)");
        return 1;
    }
    printf("exit ret=%d\n", ret);
    return ret;
}
