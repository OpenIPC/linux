static int test_rsa(int fd, struct rsa_config *prsa_config)
{
    int i = 0;

#if 1
    // reset RSA
    if (ioctl(fd, MDrv_RSA_Reset, NULL))
    {
        perror("ioctl(MDrv_RSA_Reset)");
        return 1;
    }

    // RSA calculate
    if (ioctl(fd, MDrv_RSA_Calculate, prsa_config))
    {
        perror("ioctl(MDrv_RSA_Calculate)");
        return 1;
    }
#endif

    return 0;
}

#define OUTSIZE 256

static int test_crypto(int fd)
{
    int               i;
    uint8_t *         prsaOut;
    struct rsa_config rsaConfig;
    struct rsa_config de_rsaConfig;
    uint8_t           de_rsaOut[64 * 4];
    uint8_t           rsaOut[64 * 4];

    memset(&rsaConfig, 0, sizeof(rsaConfig));
    memset(rsaOut, 0, sizeof(rsaOut));

    rsaConfig.pu32RSA_Sig    = (unsigned int *)NO_PADDING;
    rsaConfig.pu32RSA_Output = (unsigned int *)rsaOut;
    rsaConfig.pu32RSA_KeyN   = (unsigned int *)N;
    rsaConfig.pu32RSA_KeyE   = (unsigned int *)E;
    rsaConfig.u32RSA_KeyELen = sizeof(E) / 4;
    rsaConfig.u32RSA_KeyNLen = sizeof(N) / 4;
    rsaConfig.u32RSA_SigLen  = sizeof(NO_PADDING) / 4;
    rsaConfig.u8RSA_pub_ekey = 1;

    if (0 != test_rsa(fd, &rsaConfig))
    {
        printf("[%s:%d] test_rsa failed\n", __FUNCTION__, __LINE__);
        return 1;
    }

    memset(&de_rsaConfig, 0, sizeof(de_rsaConfig));
    memset(de_rsaOut, 0, sizeof(de_rsaOut));
    de_rsaConfig.pu32RSA_Sig    = (unsigned int *)rsaConfig.pu32RSA_Output;
    de_rsaConfig.pu32RSA_Output = (unsigned int *)de_rsaOut;
    de_rsaConfig.pu32RSA_KeyN   = (unsigned int *)N;
    de_rsaConfig.pu32RSA_KeyE   = (unsigned int *)D;
    de_rsaConfig.u32RSA_KeyELen = sizeof(D) / 4;
    de_rsaConfig.u32RSA_KeyNLen = sizeof(N) / 4;
    de_rsaConfig.u32RSA_SigLen  = OUTSIZE / 4;
    de_rsaConfig.u8RSA_pub_ekey = 0;
    if (0 != test_rsa(fd, &de_rsaConfig))
    {
        printf("[%s:%d] test_rsa failed\n", __FUNCTION__, __LINE__);
        return 1;
    }

    if (!memcmp(de_rsaConfig.pu32RSA_Output, NO_PADDING, sizeof(NO_PADDING)))
    {
        printf("rsa test success\n");
    }
    else
    {
        printf("input: \n");
        for (i = 0; i < 256; i++)
        {
            if (i % 16 == 0)
                printf("\n");
            printf("%02x ", (NO_PADDING[i]));
        }
        printf("\n");

        printf("output: \n");
        for (i = 0; i < 256; i++)
        {
            if (i % 16 == 0)
                printf("\n");
            printf("%02x ", de_rsaOut[i]);
        }
        printf("\n");

        printf("rsa test failed! \n");
        return 1;
    }

    return 0;
}

int main(int argc, char **argv)
{
    int fd  = -1;
    int ret = 0;

    // if (argc > 1) debug = 1;

    /* Open the crypto device */
    fd = open("/dev/rsa", O_RDWR, 0);
    if (fd < 0)
    {
        perror("open(/dev/rsa)");
        return 1;
    }

    ret = test_crypto(fd);

    /* Close the original descriptor */
    if (close(fd))
    {
        perror("close(fd)");
        return 1;
    }
    printf("exit ret=%d\n", ret);
    return ret;
}
