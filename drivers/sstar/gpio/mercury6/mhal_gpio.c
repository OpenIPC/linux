int MHal_GPIO_NameToNum(U8 *pu8Name)
{
    U8 index;
    for (index = 0; index < (sizeof(gpio_table) / sizeof(gpio_table[0])); index++)
    {
        if (!strcmp(gpio_table[index].p_name, pu8Name))
        {
            return index;
        }
    }
    return -1;
}

